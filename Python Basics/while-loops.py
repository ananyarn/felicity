# ------------------------------
# While Loop Syntax and Examples in Python
# ------------------------------

# General Syntax:
# while condition:
#     # Code block (indented)

# ------------------------------
# 1. Basic Counting Loop

i = 1
while i <= 5:
    print(i)
    i += 1

print()  # Add spacing between examples

# ------------------------------
# 2. Infinite Loop (with Break)

while True:
    print("This will run forever unless you break it!")
    break
